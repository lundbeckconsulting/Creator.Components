    public interface IIconHelper
    {
        /// <summary>
        /// Gets the size record equal to size
        /// </summary>
        /// <param name="size">Icon size</param>
        /// <returns>Record equal to size</returns>
        IIconSizeRecord GetSize(IconSize size);

        /// <summary>
        /// Gets the tag name based on output format
        /// </summary>
        string GetFormatTagName(IconOutputFormat format);

        /// <summary>
        /// Adds attributes for an anchor tag
        /// </summary>
        /// <param name="tag">Tag to process</param>
        /// <returns>Tag with added anchor attributes</returns>
        TagBuilderCustom ProcessAnchorTag(TagBuilderCustom tag, string href, TagAnchorTarget target = TagAnchorTarget.None);

        /// <summary>
        /// Gets an icon of either type FontAwesome, Friconix or Captain Icon
        /// </summary>
        /// <param name="icon">Type of icon</param>
        /// <returns>The icon</returns>
        IIconRecord GetIcon(Enum icon);

        /// <summary>
        /// Gets a formated name of the icon
        /// </summary>
        /// <param name="icon">Type of icon</param>
        /// <returns>Formated icon name</returns>
        string GetCaptainIconName(CaptainIcon icon);

        /// <summary>
        /// Gets a tag pointing to a Font Awesome icon
        /// </summary>
        /// <param name="icon"></param>
        /// <returns></returns>
        TagBuilderCustom GetFontAwesomeTag(FontAwesomeIcon icon, IconSize size = IconSize.None);
    }

