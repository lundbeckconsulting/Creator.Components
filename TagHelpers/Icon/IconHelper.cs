        public string GetFormatTagName(IconOutputFormat format)
        {
            string result = string.Empty;

            switch (format)
            {
                case IconOutputFormat.Anchor:
                    result = "a";
                    break;

                case IconOutputFormat.Button:
                    result = "button";
                    break;

                case IconOutputFormat.Div:
                    result = "div";
                    break;

                case IconOutputFormat.Span:
                    result = "span";
                    break;

                case IconOutputFormat.Italic:
                    result = "i";
                    break;

                case IconOutputFormat.Strong:
                    result = "strong";
                    break;
            }

            return result;
        }

        public TagBuilderCustom ProcessAnchorTag(TagBuilderCustom tag, string href, TagAnchorTarget target = TagAnchorTarget.None)
        {
            if (!href.Null())
            {
                tag.AddAttribute("href", href);
            }

            if (!target.Equal(TagAnchorTarget.None))
            {
                tag.AddAttribute("target", target.ToLower());
            }

            return tag;
        }

        public IIconRecord GetIcon(Enum icon)
        {
            IIconRecord result = default;

            switch (icon)
            {
                case FontAwesomeIcon fa:
                    result = _iconArchive.Single(icn => icn.FontAwesomeIcon == (FontAwesomeIcon)icon);
                    break;

                case FriconixIcon fi:
                    result = _iconArchive.Single(icn => icn.FriconixIcon == (FriconixIcon)icon);
                    break;

                case CaptainIcon ci:
                    result = _iconArchive.Single(icn => icn.CaptainIcon == (CaptainIcon)icon);
                    break;

                case DevIcon di:
                    result = _iconArchive.Single(icn => icn.DevIcon == (DevIcon)icon);
                    break;
            }

            return result;
        }

        public string GetCaptainIconName(CaptainIcon icon)
        {
            string result = ((int)icon).ToString();

            if (result.Length == 1)
            {
                result = $"00{result}";
            }
            else if (result.Length == 2)
            {
                result = $"0{result}";
            }

            return result;
        }

        public TagBuilderCustom GetFontAwesomeTag(FontAwesomeIcon icon, IconSize size = IconSize.None)
        {
            TagBuilderCustom result = new TagBuilderCustom("i");
            result.AddCssClassRange("fa-icon", _iconArchive.Single(icn => icn.FontAwesomeIcon == icon).FontAwesomePrefix, $"fa-{_iconArchive.Single(icn => icn.FontAwesomeIcon == icon).Name}");

            if (size != IconSize.None)
            {
                result.AddCssClass(_sizeArchive.Single(sz => sz.Size == size).FontAwesomeSize);
            }

            return result;
        }
